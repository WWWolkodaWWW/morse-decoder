const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let arr = [];

    for (let i = 0; i < expr.length; i = i + 10) { 
        
        let e = expr.substring(i, i + 10);

        if (e.includes('*')) { 
            arr.push(' ');
        }

        e = e.replace(/00/g, '');
        e = e.replace(/10/g, '.');
        e = e.replace(/11/g, '-');
        arr.push(MORSE_TABLE[e]);
    }

    return arr.join('');
}

module.exports = {
    decode
}